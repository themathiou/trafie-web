///////////////////////////////////////////////////////////////////////////////////////
// AlertSvc Implementation
///////////////////////////////////////////////////////////////////////////////////////
function AlertSvcImpl($rootScope, $http) {
	'use strict';

	this.alerts = [];

	//types : danger, success, info, ---can be empty
	this.addAlert = function(type, message) {
		this.alerts.push({
			"type": type,
			"msg": message
		});
	};

	this.closeAlert = function(index) {
		this.alerts.splice(index, 1);
	};

    ///////////////////////////////////////////////////////////////////////////////////////
    // Public Factory Interface for Angular
    ///////////////////////////////////////////////////////////////////////////////////////
	return this;
}
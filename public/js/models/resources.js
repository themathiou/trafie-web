angular.module('trafie.models' , []);

///////////////////////////////////////////////////////////////////////////////////////////////
// *** User ***
///////////////////////////////////////////////////////////////////////////////////////////////
angular.module('trafie.models').factory('User', function($resource) {
  return $resource('/users/:userId');
});

///////////////////////////////////////////////////////////////////////////////////////////////
// *** Activities (of User) ***
///////////////////////////////////////////////////////////////////////////////////////////////
angular.module('trafie.models').factory('Activity', function($resource) {
  return $resource('/users/:userId/activities/:activityId', {userId: '@_userId', activityId: '@_activityId'}, {
    update: {
      method: 'PUT' // this method issues a PUT request
    }
  });
});

///////////////////////////////////////////////////////////////////////////////////////////////
// *** Disciplines (of User) ***
///////////////////////////////////////////////////////////////////////////////////////////////
angular.module('trafie.models').factory('Discipline', function($resource) {
  return $resource('/users/:userId/disciplines/:activityId');
});


///////////////////////////////////////////////////////////////////////////////////////////////
// *** Settings (of User) ***
///////////////////////////////////////////////////////////////////////////////////////////////
angular.module('trafie.models').factory('Setting', function($resource) {
  return $resource('/settings_data');
});